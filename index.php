<!--        Scrolling down Button     -->
        <script type="text/javascript">
        $(document).ready(function(){

            function whenScrolled(){
                var b_height = $('.background').height();
                var w_height = $(window).height();
                var scrolled = $(window).scrollTop();
                if(scrolled <= w_height){
                    var op       = $('.center_box').css('opacity');
                    $('.center_box').css('margin-top' , scrolled + 'px');
                    $('.center_box').css('opacity' , (b_height / scrolled) / 10);
                    $('.chevron_container i').css('opacity' , (b_height / scrolled) / 10);
                    $('.page_two').css('height' , scrolled);
                    
                }

                if(scrolled > w_height){
                    $('.page_two').css('height' , w_height - (scrolled - w_height) + 'px');
                }

                if(scrolled > w_height && scrolled < w_height * 2){
                    scrolled = scrolled - w_height;
                    //alert('s');
                    $('.page_two').css('height' , w_height - scrolled + 'px');
                }
            }

            whenScrolled();


            $(window).scroll(function(){
                whenScrolled();
            });

            $('.background .chevron_container i').click(function(){
                 $('html,body').animate({
                    scrollTop: $(window).height()
                }, 1200);
            });
        });
    </script>
        
