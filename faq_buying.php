<h2  id="p1" style="padding-left: 200px"> 1. How do I find something to bid on and then make a bid? </h2><br>
<p style="padding-left: 200px;padding-right: 200px"> You can search for auctions using the search box at the top of every page. You can browse for auctions using the categories listed below the search box on every page.</p>

<h2  id="p2" style="padding-left: 200px"> 2. How can I refine my search?</h2><br>
 <p style="padding-left: 200px;padding-right: 200px" > Once you have added a search string and see the list of search results you will be given the option to refine the results. Above and to the right of the result listings you will see the "More Search Options" link. This allows you to filter your results by Price, Feedback of the Seller and Closed Auctions amongst other options.


</p>

<h2  id="p3" style="padding-left: 200px">  3. How can I save my favourite Sellers, Stores, Listings and Searches?</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > My Favourite Sellers

You can save a Seller to your saved lists by clicking on the seller username and selecting "Add them to Favourite Sellers" from the seller page option menu. Your favourite sellers will appear on the My Favourite Sellers section at "My Nafah > My Favorites & Lists". You can also edit and delete your Favourite Sellers here. The edit option allows you to amend the listing email alert and listing type options. <br><br>

My Favourite Nafah Stores <br><br>

You can save a Sellers Store to your saved list by clicking on the Storename link to open the customer Store page and selecting the "Add to My Favourite Stores" link. You will have the option to remove saved Stores by going to "My Nafah > My Favorites & Lists". <br><br>

My Favourite Listings <br><br>

You can add individual listings to your Favourite Lists by selecting the "Add to List" button on the listing page. The button can be found just under the photo at the top left hand side of the page. You can add multiple lists which can be viewed and removed from the "My Nafah > My Favorites & Lists" section. <br><br>

My Favourite Searches <br><br>

Once you have made a search and can see the resulting listings you will see the link "Add to My Favourite Searches" above the listings. This will take you to the Favorite Searches page where you can add additional criteria to your search string. To view your favorite searches please go to "My Nafah > My Favorites & Lists". 
</p>

<h2  id="p4" style="padding-left: 200px"> 4. Can I receive an email alert when something I want to buy is put up for sale on Nafah?</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > Once you have made a search and can see the resulting listings you will see the table "Refine your Search" to the right of the listings. Under this table please select the "Buyer tip. Get emailed daily with stuff being sold to do with *searchname*" link. This will take you to the Favorites Searches page where you can add additional criteria to your search string, including receiving emails on the search for a set period of time or daily.


</p>

<h2  id="p5" style="padding-left: 200px"> 5. I get outbid every time I place a bid.</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > Actually, there is nothing wrong. Being immediately outbid means that an earlier bidder specified a maximum bid higher than the maximum you just specified. When you confirmed your bid, the other bidder"'s "proxy" immediately placed a bid on his behalf, just enough to outbid your maximum, to ensure his position as high bidder. When you place a bid with a maximum high enough to outbid all other bidders, your proxy will protect your position in the same way.<br><br>
If you place a bid that matches a previously placed Proxy bid, you may see the same price appear on the auction bid history for both bidders. In this instance the earliest placed bid takes precedence.


</p>

<h2  id="p6" style="padding-left: 200px"> 6. How do I find out how my auction ended?</h2><br>
<p style="padding-left: 200px;padding-right: 200px" >You will be emailed within 15 minutes of the auction closing time. Auction results are also available in the My Nafah Auction Control Centre. Login by clicking the My Nafah link at the top of each page.


</p>

<h2  id="p7" style="padding-left: 200px"> 7. Can I make an offer to buy something from a seller?</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > Yes you can. As long as the seller turned on our "Make an Offer" system when they started the listing.<br><br>

A button "Make an Offer" will appear on the listing just under the BuyNow button. When you click this you can enter your offer amount, must be below the BuyNow price. The seller will be emailed with the details and will have 48hrs to either <br><br></p>
<ul style="display: inline-block;padding-left: 200px">
  <li>Accept Offer</li>
  <li>Decline Offer</li>
  <li>Make a Counter-offer</li>
</ul>
<br>
<p style="padding-left: 200px;padding-right: 200px" >
You will be emailed with their decision. If they accept, then the normal after sale system will kick in. If they make a counter-offer (will be above your original offer and below the BuyNow price), you will then have 48hrs to accept or decline their counter-offer.
</p>

<h2  id="p8" style="padding-left: 200px"> 8. Can I ask the seller a question?</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > If you go to the page where the item is listed and click on the "ask seller a question" button there you will be able to ask the seller a question via email. An email will be sent to the seller automatically with your question and inviting the seller to post an answer. As soon as they have posted a reply, you will receive an email with this information.
<br><br>
Please note that all questions and replies on the Q and A section of the auction page can be viewed publicly. Please do not pass any email address or contact details via this section. Contact details should only be passed on a successful auction, after the auction has closed via the After Sale Log.
</p>



