<h2  id="p1" style="padding-left: 200px"> 1. Can I really list for free? </h2><br>
<p style="padding-left: 200px;padding-right: 200px"> Yes. On Nafah it is free to list your item. After you have joined you will need to upgrade your account from BUYER to either SELLER (free but card validation required) or SELLER+ (security subscription required). 
<br><br>
A SELLER account gives you access to our "Standard" listing format which is free to list with a 3% Final Value Fee. If you pay to upgrade to SELLER+ you can use our "Free Listing" which is also free to list but also has zero Final Value Fee. See our Registration FAQ page for more details on upgrading your account. SELLER+ subscriptions can be purchased for 7, 30, 90, 365 days or a Lifetime giving you zero listing fees and zero final value fees for life. Check out our current special offer for half price SELLER+ Lifetime upgrades. No more fees for life.</p>

<h2  id="p2" style="padding-left: 200px"> 2. How do I start a listing on Nafah?</h2><br>
 <p style="padding-left: 200px;padding-right: 200px" > Simple answer is to click here and choose which category you want to place your item(s) into.
<br><br>
All sellers must have SELLER or SELLER+ access, that means they must have a credit or debit card validated with us. We run these through the secure servers of Netbanx.com, Paypoint.net or PayPal to allow us to check the authenticity of the users selling on Nafah. This cuts out fraud dramatically and stops sellers spamming the site with worthless listings.
<br><br>
Once you have SELLER or SELLER+ status then click the link to SELL at the top of the page and choose your category. Fill in a title, description, price, shipping costs and the payment methods you wish to use to receive money (see below for details on payment processors you can accept on Nafah).
<br><br>
Canadian customers with a SELLER+ Lifetime subscription will have the option to change their listing currency. As well as the home currency of CAD, USD and GBP are also available. This option is made from Section One of the listing page. Customers posting listings using the Bulk Upload option should refer to the example spreadsheet help section that can be found at My Nafah > My Bulk Upload.


</p>

<h2  id="p3" style="padding-left: 200px">  3. How can I edit listings I'm running?</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > Click on "Edit/Close Listing" on the individual page for the listing you wish to change. Enter your details, some things cannot be changed once a bid has been placed. If you have posted an listing with a quantity of 1 (single-item auction) you will not be able to edit the quantity to a larger amount. Listings with quantities greater than 1 are multi-item listings and have a different bidding process, these can be changed to any quantity between 2 and 99. 
<br><br>
There is also an option to Bulk Edit multiple listings at once- Go to My Nafah > My Bulk Edit for more information.
</p>

<h2  id="p4" style="padding-left: 200px"> 4. How do I contact the buyer?</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > You will be automatically sent the buyers full contact details by email when your listing reaches its end.
</p>

<h2  id="p5" style="padding-left: 200px"> 5. How do I add a description to my listings?</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > When posting your listings we have three types of Description Editors available for entering and editing information : Plain HTML Box, WYSIWYG Simple and WYSIWYG Advanced. Your favoured method can be selected when posting listings by using the dropdown menu in the 'Listing Description' section. You can also set your editor default in the My Defaults section. To do this go to My Nafah - My Defaults (left hand side menu) and select your preference in the General Defaults section. There is a limit of 500000 characters to a single listing description If you are adding your own HTML to this editor please note that any tags before the <body> tag in your code will be stripped so if you have stylesheets make sure you put them in the body of your code.
<br><br>
Plain HTML Box : If you already have HTML prepared then this can be pasted into Plain HTML Box. Your code will not be formatted or changed in any way.
<br><br>
WYSIWYG Simple : This will enable you to get your auction up and running with the minimum of fuss. What you type into the description area is how the auction will be shown when the auction is posted. There are a minimum number of Tool icons available but sufficient to get a no frills auction on site quickly and efficiently.
<br><br>
WYSIWYG Advanced : Similar to the WYSIWYG Simple option but with far more editing and HTML options available to enhance your auctions. We recommend using this if you are building your listing descriptions from scratch. If you wish to copy and paste HTML or text from Microsoft Word into this editor it will be subject to the formatting rules of the editor and may not be a 100% match to your original document. Some tags will be stripped from your HTML when using this method. We recommend the Plain HTML Box if you already have your description HTML ready.
</p>

<h2  id="p6" style="padding-left: 200px"> 6. How can I add my new and existing listings to my store?</h2><br>
<p style="padding-left: 200px;padding-right: 200px" >To add your existing listings to your Nafah store, go to "My Nafah" > "Listings I'm Running", then click on the edit link to the right of the listing you wish to add to your store. From the edit page you can add the listing to your store by using the Nafah store category dropdown menu in section One. To add a new listings to your store when posting a new listing, in Section One of the auction listing page select the store category/sub category from the drop down menu. Your store will need to be online to be able to select a category.
</p>

<h2  id="p7" style="padding-left: 200px"> 7. How can I pay or accept payment via Google Wallet/Checkout?</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > Effective from November 20th 2013 Google has shut down its Google Checkout product. We are therefore no longer offering Google Checkout as a payment option for customers. Please check the Google Checkout website for more information.
</p>

<h2  id="p8" style="padding-left: 200px"> 8. How can I add a sale/mark down/discount to my items?</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > Discounts can be added to BuyNow prices for between 0.1% and 99.9%. Maximum time is 45 days.
SELLER+ users can see the discount facility on the edit page of a single item OR on the My Bulk Edit page.
Discounts will be carried over on auto-reposts. Single-reposts and mass-reposts will not carry any discount data over.
</p>

<h2  id="p9" style="padding-left: 200px"> 9. How can I Close or Delete all my listings at once?</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > f you are running a large number of auctions that you would like to Close or Delete, we would suggest using the Bulk Edit option.<br><br>
Go to My Nafah > My Bulk Edit (left hand menu), add your auctions to the Edit Bucket and use the Delete All Auctions or Close All Auction buttons at the foot of the page.
<br><br>
The other option for customers not running a large number of auctions is to Close or Delete via the Listings I'm Running section.
Go to My Nafah > Listings I'm Running (left hand menu) then use the All Delete or All Close buttons at the top right hand side of the section then use the Close/Delete Selected Auctions button at the foot of the page.


