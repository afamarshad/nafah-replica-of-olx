<h2  id="p1" style="padding-left: 200px"> 1. How do I register with Nafah </h2><br>
<p style="padding-left: 200px;padding-right: 200px"> It's simple. Just go to the registration page, fill out the form, choose which account plan you want (BUYER, SELLER or SELLER+) and submit it. You'll receive a confirmation link by email that you'll need to click on then you will then have unlimited access to Nafah.To ensure you are registering on the correct Nafah site please go to the Nafah.net country selection page and select your country of residence.
<br><br>
Register as BUYER only you'll be able to bid and buy, not sell.
<br><br>
If you are listing an item for auction you will need to either upgrade to SELLER or SELLER+, see next FAQ below. A comparison of Nafah account plans can be seen below also.</p>

<h2  id="p2" style="padding-left: 200px"> 2. How do I upgrade my account?</h2><br>
 <p style="padding-left: 200px;padding-right: 200px" > You can upgrade immediately when you register by choosing SELLER or SELLER+ on the registration form. To upgrade any time after registering visit the "My Subscriptions" section of "My Nafah". The details for each account plan can be seen below. This page will also show you what current plan you are on.
<br><br>
SELLER : upgrade fee : Lifetime $0.00
<br><br>
SELLER+ : upgrade fee :7 Day $1.99   |   30 Day $6.99   |   90 Day $16.99   |   365 Day $54.99   |   Lifetime $99.98 
<br><br>
SELLER+ Lifetime is currently available for $49.99 if upgrade takes place within 24 hours of registration OR any other upgrade.
<br><br>
Please note that all SELLER+ subscriptions apart from lifetime are recurring. SELLER+ subscriptions may be cancelled at any time via the dropdown menu on the "My Nafah" > "My Subscription" section.
<br><br>
If you require an invoice for your subscription, this can be printed from the "My Nafah" > "My Subscription" > "Print Invoice" link at the bottom right hand side of the page.

</p>

<h2  id="p3" style="padding-left: 200px">  3. I've forgotten my password</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > If your username and password don't seem to be working, firstly check you are entering it correctly, you'll be amazed how many people mistake a small "L" for a capital "i" or have an incorrect computer clock. Secondly you can visit our reminder page where you can enter your username or email address and be sent a password reset link by email to your registered email address. The email you receive will identify the IP address of the person who requested the reset. You can find your IP address here if you wish to confirm it came from you.

</p>

<h2  id="p4" style="padding-left: 200px"> 4. How can i change my username?</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > If you have registered and have made a mistake with your username or have a valid reason for changing, please contact us via the "Contact Us" tab on our "Help" index page section and if applicable we will make the necessary changes. There is a limit of 18 characters. We cannot accept spaces in the Username and characters such as stops and hyphens are not compatible, although underscores are acceptable. For security reasons, on the Help Section Contact Form please ensure you enter your registered email address.


</p>

<h2  id="p5" style="padding-left: 200px"> 5. I want to choose my own password.</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > When you first register you choose your own password. It can be changed on the "My Nafah" > "My Personal Details" page.
</p>

<h2  id="p6" style="padding-left: 200px"> 6. I can't login to the Nafah Forums area.</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > When you first register you choose your own password. It can be changed on the "My Nafah" > "My Personal Details" page.
</p>

<h2  id="p7" style="padding-left: 200px"> 7. Do I need to give you a credit card?</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > Registration is FREE. If you are listing an item for auction you will need to upgrade your account to SELLER or SELLER+ status, each will give you differing access to our selling facilities. A credit or debit card can be used via our secure payment providers. A credit or debit card is currently the only method of obtaining a Security Subscription. SELLER requires a free validation of your card, SELLER+ requires a 7,30,90,365 day or Lifetime Security Subscription to be purchased. To see current SELLER+ Security Subscription prices and to upgrade view the My Subscriptions section of My Nafah.
</p>

<h2  id="p8" style="padding-left: 200px"> 8. I've been told I need to validate my account.</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > On the email you receive when you register there is a link you must click on that tells us you received the email ok. If you haven't done this yet then do it now. The email should arrive immediately after you register. If you haven't received the email then go to our lost password page. You can enter your username or email address here and submit. The validation email will be sent out to you again.


</p>

<h2  id="p9" style="padding-left: 200px"> 9. I've been told my account is on Hold.</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > To keep Nafah as clutter free to allow genuine sellers a clean and efficient marketplace in which to trade, we ask all sellers to not break our posting rules <br><br>
If any of your items cross this boundary, ALL your auctions will be deleted without notice and your account placed on hold. When you try and login while your account is on hold you will find you are locked out. To remove this ban, please email us using the contact form in the Help section stating the item(s) you posted to create this ban and that you will not post such items any more. Please do not email us asking us what you did wrong, a simple apology will re-open your account almost immediately whereas a long winded email conversation will waste ours and your time.<br><br>
If you state you will refrain from posting banned items and are caught a second time, the same will happen.
If you are caught a third time your account will be locked on a permanent basis. It is up to you to read the posting rules and abide by them, users are told about the posting rules when they start an auction. "I didn't realise" won't be a valid excuse!
Continuous negative feedback will also see your account placed on hold. Always make sure your first feedback is positive otherwise your account could be investigated.<br><br>
Holders of multiple accounts will also be placed on hold.<br><br>
Users suspected of bidding on their own auctions (shilling or shill bidding) will see their accounts placed on hold.
Users with incomplete address details will be placed on hold.


</p>

<h2  id="p10" style="padding-left: 200px"> 10. Is it free to join Nafah?</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > Joining Nafah is completely free, just sign up with our form here. You can be bidding within a few seconds. Once you have joined then you will have the option to upgrade to SELLER or SELLER+ status to allow yourself to start selling.


</p>

<h2  id="p11" style="padding-left: 200px"> 11. How do you assure the privacy of my information?</h2><br>
<p style="padding-left: 200px;padding-right: 200px" > We value our own privacy and yours, and do not abuse your information. Specifically: <br><br>
<ul style="display: inline-block;padding-left: 200px"> 
<li>We disclose your information to other Nafah users only when a sale is made between two parties.</li>
<li>All the personal data entered is recorded and stored in a secure database.</li>
<li>We will not sell or give your name to other organizations for them to contact you.</li>
<li>We will disclose your contact information to other users who have a valid reason to contact you.</li>
<li>We will use your contact information to send you invoices plus any notifications, newsletters and announcements you opt into via My Nafah > My Notifications.</li>
</ul>
